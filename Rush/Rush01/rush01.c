void	as_line(char x, int x2);

void	bs_line(char x, int x2);

int	rush( char x, char y)
{
	int	x2;
	int	y2;

	x2 = 1;
	y2 = 1;
	while (y2 <= y)
	{
		if (y2 == 1 || y2 == y)
		{
			as_line(x, x2);
			y2++;
			x2 = 1;
		}
		else if (y2 > 1 && y2 < y)
		{
			bs_line(x, x2);
			y2++;
			x2 = 1;
		}
	}
	return (0);
}
