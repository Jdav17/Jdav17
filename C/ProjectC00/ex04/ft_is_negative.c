#include <unistd.h>

void	ft_is_negative(int n)
{
	char	p;
	char	n;

	n = 'N';
	p = 'P';
	while (n >= 0 || n < 0)
	{
		if (n >= 0)
		{
			write(1, &p, 1);
			break ;
		}
		else
		{
			write(1, &n, 1);
			break ;
		}
	}
}
